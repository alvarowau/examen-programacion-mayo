package org.alvarowau.models;

/**
 * La enumeración Tipo representa los tipos de electrodomésticos grandes.
 * Puede ser de instalación o sin instalación.
 */
public enum Tipo {
    INSTALACION, // Tipo de electrodoméstico grande que requiere instalación.
    NO_INSTALACION; // Tipo de electrodoméstico grande que no requiere instalación.
}
